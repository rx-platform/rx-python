def error(error):
    
    if error=='Connection error':
        print('Error 256: Connection Error')
    elif error=='Invalid message':
        print('Error 255: Invalid Message')
    elif error=='Message size':
        print('Error 254: Message You Are Trying To Send Is Too Large')
    elif error=='Connection closed':
        print('Error 253: Connection To The Server Has Been Forcefully Closed')
    elif error=='Ack error':
        print('Error 252: Server Did Not Respond Properly')
    
    
  

   
      

 
      

 


